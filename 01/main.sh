#!/bin/bash

# Проверка количества аргументов
if [ $# -ne 1 ]; then
    echo "ОШИБКА: необходимо указать ровно один текстовый параметр."
    exit 1
fi

param="$1"

# Проверка: параметр не должен быть числом
if [[ "$param" =~ ^[0-9]+$ ]]; then
    echo "ОШИБКА: параметр не должен быть числом."
    exit 1
fi

# Вывод параметра
echo "Параметр: $param"

exit 0
